/**
* @brief Function implementing the Read_AoA thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_task1_read_aoa */
void task1_read_aoa(void *argument)
{
  /* USER CODE BEGIN task1_read_aoa */
  /* Infinite loop */
	// High Priority Task to read the aoa sensor (potentiometer)
	while(1)
		{
		  // While reading ... blue led blinks
		HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_7);

		  // Read value from potentiometer 1 (AoA Sensor)
		  HAL_ADC_Start(&hadc1);
		  HAL_ADC_PollForConversion(&hadc1, 1);
		  ADC1RES = HAL_ADC_GetValue(&hadc1);

		  // Convert digital value to AoA. pot range [0..270]
		  // point_a = (6,0)
		  // point_b = (4094,270)
		  AoA_actual = convert_adc_to_physical_value(ADC1RES,6,0,4094,270);


		  // horizontal position of the aircraft correspond to the 135ยบ, so that, let's convert it to 0ยบ
		  if (AoA_actual < 135)
			  AoA_actual = -1*AoA_actual;
		  else
			  AoA_actual = AoA_actual - 135;

		  // Signal consumers that AoA os ready for reading
		  osEventFlagSet(writing_AoA);


		  // do one read every seconds
		  osDelay(1000);
		}
  /* USER CODE END task1_read_aoa */
}

